"""Короткие брифы тренировок для отправки в Telegram.

Когда план содержит силовую / кор / плиометрику — бот шлёт второе сообщение
с протоколом: какие упражнения, режим работы/отдыха, ключевые правила.

Источник параметров — `plans/2026_may_dec_operational.md`, секции «Ключевые
сессии — протоколы».
"""
from __future__ import annotations

import re
from typing import Optional

from .plan_reader import PlannedSession


def for_session(session: PlannedSession) -> Optional[str]:
    """Подобрать бриф под тип сессии. None если для типа брифа нет."""
    t = (session.type or "").lower()
    if t in ("ст-омв", "ст-омв-пик"):
        return _brief_st_omv(session)
    if t == "ст-тон":
        return _brief_st_ton(session)
    if t == "кор":
        return _brief_kor(session)
    if t == "плиометрика":
        return _brief_plyo(session)
    return None


def for_today(sessions: list[PlannedSession]) -> list[str]:
    """Все брифы для сессий дня.

    1. По типу сессии (ст-омв / кор / плиометрика и т.д.)
    2. Дополнительно: если в тексте упоминается «кор-стабилизация» или
       «прыжковая» — добавить соответствующий бриф (если ещё не добавлен).
       Нужно потому что в Excel некоторые из них «приклеены» к утренней
       беговой строке (тип `бег`), а не идут отдельной записью.
    """
    out: list[str] = []
    seen_types: set[str] = set()

    for s in sessions:
        b = for_session(s)
        if b:
            out.append(b)
            seen_types.add(s.type)

    # Текстовый fallback
    has_kor = any("кор-стабилизац" in (s.text or "").lower() for s in sessions)
    has_plyo = any(
        "прыжков" in (s.text or "").lower() or "плиометр" in (s.text or "").lower()
        for s in sessions
    )

    if has_kor and "кор" not in seen_types:
        out.append(_brief_kor(sessions[0]))
    if has_plyo and "плиометрика" not in seen_types:
        out.append(_brief_plyo(sessions[0]))

    return out


# ----------------------------------------------------------------------
# СТ ОМВ — статодинамика (май-июнь)
# ----------------------------------------------------------------------

def _brief_st_omv(session: PlannedSession) -> str:
    text = session.text or ""
    is_low = "НИЗ" in text.upper()
    is_high = "ВЕРХ" in text.upper()

    # Сколько кругов из текста (e.g. "5 кругов ПИК")
    circles_match = re.search(r"(\d+)\s*(?:круг|кругов)", text, re.IGNORECASE)
    circles = circles_match.group(1) if circles_match else "?"
    is_pik = "пик" in text.lower()
    is_intro = circles == "3"  # 3 круга = втягивающая

    intensity = "40% МПС" if is_intro else ("50-60% МПС" if not is_pik else "55-65% МПС")
    set_dur = "30 сек" if is_intro else "30-40 сек"
    rest_between_sets = "25 сек"
    rest_between_circles = "5-7 мин" if is_intro else ("6-8 мин" if not is_pik else "7-10 мин")

    if is_low:
        focus = "🦵 НИЗ"
        exercises = [
            "Приседы (без полного разгибания)",
            "Выпады попеременно (статодинамика)",
            "Разгибания ног на тренажёре (медленно, без отдыха в верхней точке)",
            "Икры (на одной ноге, медленно)",
            "Планка 40 сек",
        ]
    elif is_high:
        focus = "💪 ВЕРХ"
        exercises = [
            "Тяга в наклоне (резинка/гантель, медленно)",
            "Отжимания (без касания пола, без распрямления)",
            "Жим/махи на плечи",
            "Трицепс (разгибание из-за головы)",
            "Пресс-кор (скручивания/планка с подъёмом ноги)",
        ]
    else:
        focus = "общий"
        exercises = ["см. протокол СТ ОМВ — приседы/выпады/тяга/планка/мост"]

    pik_marker = " (ПИК блока)" if is_pik else ("" if not is_intro else " (втягивающая)")

    lines = [
        f"🏋 СТ ОМВ{pik_marker} — БРИФ",
        f"Фокус: {focus} | {circles} круга × 5 упр",
        "",
        "⚙️ Параметры подхода:",
        f"  • {set_dur} работы, {intensity}",
        f"  • МЕДЛЕННО, без полного разгибания, без расслабления, ДО ОТКАЗА",
        f"  • 3 подхода через {rest_between_sets} отдыха",
        f"  • Между кругами — {rest_between_circles} активного отдыха",
        "",
        "📋 Упражнения:",
    ]
    for i, ex in enumerate(exercises, 1):
        lines.append(f"  {i}. {ex}")

    lines.extend([
        "",
        "🎯 Ключевой контроль:",
        "  • Жжение в мышце к 25-30 сек подхода",
        "  • Если жжения нет к 35 сек → увеличить вес/уменьшить амплитуду",
        "  • Дыхание ровное, не задерживать",
    ])
    return "\n".join(lines)


